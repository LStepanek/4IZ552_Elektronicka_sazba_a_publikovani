\#\# instaluji a inicializuji balíčky -------------------------------------------

for(package in c(
                 "openxlsx",
                 "xtable",
                 "tm"
                 )){
                 
    if(!(package %in% rownames(installed.packages()))){
    
        install.packages(
            package,
            dependencies = TRUE,
            repos = "http://cran.us.r-project.org"
        )
        
    }
    
    library(package, character.only = TRUE)
    
}


