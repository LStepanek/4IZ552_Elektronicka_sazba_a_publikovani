#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#

#\#\#\# zadávám, kolik prvních "my\_proportion" * 100 procent každého korpusu
#\#\#\# bude zpracováno pro účely sestavení vektoru s větami ---------------------

my_proportion <- 0.005    #\# zřejmě je 0 <= my\_proportion <= 1


#\#\#\# -------------------------------------------------------------------------

#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#\#

#\#\#\# inicializuji vektor "my\_sentences", do kterého se budou ukládat
#\#\#\# věty všech korpusů -------------------------------------------------------

my_sentences <- NULL


#\#\#\# nyní dělím všechny korpusy na jednotlivé věty (i vedlejší) ---------------

for(my_corpus_source in c(
    
    "blogs",
    "news",
    "twitter"    

)){
    
    # '''
    # postupně upravuji a očišťuji daný korpus
    # '''
    
    #\# loaduji korpus ---------------------------------------------------------
    
    my_corpus <- get(paste(my_corpus_source, "corpus", sep = "_"))
    
    
    #\# dělím korpus na věty ---------------------------------------------------
    
    for(i in 1:ceiling(length(my_corpus) * my_proportion)){
        
        my_sentences <- c(
            my_sentences,
            splitTextIntoSentences(my_corpus[i])
        )
        
        flush.console()
        print(
            paste(
                "Korpus '",
                my_corpus_source,
                "' (jeho první ",
                format(
                    round(
                        my_proportion * 100,
                        digits = 1
                    ),
                    nsmall = 1
                ),
                " %): ",
                "proces hotov z ",
                format(
                    round(
                        i / ceiling(
                            length(my_corpus) * my_proportion
                        ) * 100,
                        digits = 2
                    ),
                    nsmall = 2
                ),
                " %.",
                sep = ""
            )
        )        
        
    }
    
}


